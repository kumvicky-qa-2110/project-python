
a = 34
b = 2 

# Arithmetic Operator 
print("a + b = ", a + b)
print("a - b = ", a - b)
print("a * b = ", a * b)
print("a / b = ", a / b)
print("a % b = ", a % b)
print("a // b = ", a // b)
print("a ** b = ", a ** b)
print("-------")

# Conditinal Operator
